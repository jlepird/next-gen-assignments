    <h3>Testing a new talent matching algorithm</h3>
        <p>The backbone of this assignment system is to test a talent matching algorithm that maximizes satisfaction between both officers and the billet owners themselves. This algorithm has been used successfully for nearly 50 years by the <a href="http://www.nrmp.org/match-process/match-algorithm/"> National Residency Matching Program</a>, the process by which graduating medical students are awarded their initial placement in hospitals. </p> 
        
        <p> To allow the algorithm to function at its maximum potential, it requires cooperation from both parties.  Officers need to create a 1-to-n list of assignments that they want, and billet owners need to create a 1-to-n list of officers that they want.  The proposed algorithm would then require further modifications by the AFPC Assignment Team as necessary to incorporate additional constraints such as unit manning requirements.</p>
    <h3>Helping officers "shop" for their next assignment</h3>
        <p>To aid in the creation of preferential assignment lists, this test also incorporates an improved way to "shop" for assignments compared to the current AMS system.  The <a href="../billets/find.php">Find a Billet</a> link allows officers to filter and view details about the assignments that are available.  As you will notice, the information listed for each assignment is more than what is typically shown for assignments during your past VML cycles.  This information is collected from the billet owners themselves using the <a href="../billets/manage.php">Manage Billets</a> link.  As such if you are a billet owner, please provide as much detail about the assignment as possible, since this will allow officers to focus on assignments that are most suitable for their careers, and ultimately the Air Force.</p>

        

